// Write a function to return the name of a person with the most male friends
// If there are more than 1 person with the highest male friend count, return any one of them.
function findPersonWithMostMaleFriends(input) {
    const guidAndGenderMap = {};

    // create a Map using guid & gender
    input.forEach((person) => {
        guidAndGenderMap[person.guid] = person.gender;
    });

    // console.log(guidAndGenderMap);

    let currentHighestMaleFriendCount = 0;
    let personWithHighestMaleFriendCount = "";

    input.forEach((person) => {
        let maleFriendCount = 0;

        person.friends.forEach((friendGUID) => {
            if (guidAndGenderMap[friendGUID] === "male") maleFriendCount++;
        });

        if (maleFriendCount > currentHighestMaleFriendCount) {
            // re-assign currentHighestMaleFriendCount with current maleFriendCount of the person being looked at
            currentHighestMaleFriendCount = maleFriendCount;
            // re-assign mostPopular with the person being looked at
            personWithHighestMaleFriendCount = person.name;
        }
    });

    return personWithHighestMaleFriendCount;
}

